// Trivial class for keeping key and value pairs together.
// Probably ought to be generic, but for simplicity we don't.
//Hai Vo hqvo@ucsc.edu
class pair {

   String key;
   String value;

   pair (String _key, String _value) {
      key = _key;
      value = _value;
   }

}

